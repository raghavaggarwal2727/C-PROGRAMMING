/*Write a program to print the following pattern:

*

*
*
*

*
*
*
*
*

*
*
*

* */
#include <stdio.h>

int main() {
    // First star
    printf("*\n\n");

    // 2nd block: 2 stars
    printf("*\n*\n\n");

    // 3rd block: 5 stars
    printf("*\n*\n*\n*\n*\n\n");

    // 4th block: 3 stars
    printf("*\n*\n*\n\n");

    // Last single star
    printf("*\n");

    return 0;
}


